const table = "job_seeker_educations";

exports.create = async (data) => {
  return new Promise((resolve, reject) => {
    DB(table)
      .insert(data)
      
      .then(function (record) {
        resolve({ status: true, data: record });
      })
      .catch(function (error) {
        resolve({ status: false, data: error });
      });
  });
};

exports.update = async (query, data) => {
  return new Promise((resolve, reject) => {
    DB(table)
      .where({ ...query })
      .update(data)
      .then(function (record) {
        resolve({ status: true, data: record });
      })
      .catch(function (error) {
        resolve({ status: false, data: error });
      });
  });
};

exports.getOne = async (query = {}, notQuery = {}) => {
  return new Promise((resolve, reject) => {
    DB(table)
      .where(query)
      .whereNot(notQuery)
      .first()
      .then(function (record) {
        resolve(record);
      })
      .catch(function (error) {
        console.log(error);
        resolve(null);
      });
  });
};

exports.getOneDetailed = async (query = {}, notQuery = {}) => {
  return new Promise((resolve, reject) => {
    DB(table)
      .select(
        "job_seeker_educations.*",
        "countries.name as country_name",
        "graduation_certificates.name as graduation_certificate_name",
        "grades.name as grade_name"
      )
      .leftJoin(
        "countries",
        "countries.id",
        "=",
        "job_seeker_educations.country"
      )
      .leftJoin(
        "graduation_certificates",
        "graduation_certificates.id",
        "=",
        "job_seeker_educations.graduation_certificate"
      )
      .leftJoin("grades", "grades.id", "=", "job_seeker_educations.grade")
      .where(query)
      .whereNot(notQuery)
      .first()
      .then(function (record) {
        resolve(record);
      })
      .catch(function (error) {
        console.log(error);
        resolve(null);
      });
  });
};

